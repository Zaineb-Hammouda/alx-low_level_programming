#include "main.h"
/**
 * add - adds two numbers
 * @a: first num
 * @b: second num
 * Return: the result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substract two numbers
 * @a: first num
 * @b: second num
 * Return: the result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mutiplies two nums
 * @a: first
 * @b: second
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two nums
 * @a: first
 * @b: second
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - gives remainder of division
 * @a: first
 * @b: second
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
